    
    
    ////// bitwise left shift/////

    let x=12

    //using simple multiplication
    let y=x*2;
    // using left shift
    let z=x<<1
    
    //both y and z gives the same result
    console.log(`value of y is ${y} and value of z is ${z}`);



    ////bitwise right shift ////////


    let a=12

    //using simple division
    let b=a/2

    //division using left shift
    let c=a>>1
  
    //both y and z gives the same result
    console.log(`value of b is ${b} and value of c is ${c}`);
